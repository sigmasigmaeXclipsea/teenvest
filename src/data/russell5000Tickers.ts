// Get all sectors from the ticker list
export const getAllSectors = (): string[] => {
  const sectors = new Set<string>();
  russell5000Tickers.forEach(t => {
    if (t.sector) sectors.add(t.sector);
  });
  return Array.from(sectors).sort();
};

// Search tickers by symbol or name
export const searchTickers = (query: string, limit = 20): TickerInfo[] => {
  const q = query.toLowerCase().trim();
  if (!q) return popularTickers.slice(0, limit);
  
  // First, exact symbol matches
  const exactMatches = russell5000Tickers.filter(t => 
    t.symbol.toLowerCase() === q
  );
  
  // Then, symbol starts with query
  const symbolStartsWith = russell5000Tickers.filter(t => 
    t.symbol.toLowerCase().startsWith(q) && 
    !exactMatches.includes(t)
  );
  
  // Then, name contains query
  const nameContains = russell5000Tickers.filter(t => 
    t.name.toLowerCase().includes(q) && 
    !exactMatches.includes(t) && 
    !symbolStartsWith.includes(t)
  );
  
  return [...exactMatches, ...symbolStartsWith, ...nameContains].slice(0, limit);
};

// Get ticker info by symbol
export const getTickerInfo = (symbol: string | undefined | null): TickerInfo | undefined => {
  if (!symbol) return undefined;
  return russell5000Tickers.find(t => t.symbol.toUpperCase() === symbol.toUpperCase());
};

// Get total count of tickers
export const getTotalTickerCount = (): number => {
  return russell5000Tickers.length;
};
